import React, { useState, useEffect } from "react";

const ClientOnlyComponent = ({ children }: { children: React.ReactNode }) => {
  const [isClient, setIsClient] = useState(false);

  useEffect(() => {
    setIsClient(true);
  }, []);

  if (!isClient) {
    return null;
  }

  return children;
};

export default ClientOnlyComponent;
